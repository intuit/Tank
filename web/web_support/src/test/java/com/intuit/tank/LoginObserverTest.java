import com.intuit.tank.project.User;
import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;
import org.mockito.InjectMocks;
import org.mockito.Mock;
import org.mockito.MockitoAnnotations;

import static org.junit.jupiter.api.Assertions.*;
import static org.mockito.Mockito.*;

public class LoginObserverTest {

    @InjectMocks
    private LoginObserver loginObserver;

    @Mock
    private PreferencesBean preferencesBean;

    private AutoCloseable closeable;

    @BeforeEach
    void setUp() {
        closeable = MockitoAnnotations.openMocks(this);
    }

    @AfterEach
    void tearDown() throws Exception {
        closeable.close();
    }

    @Test
    public void testObserveLogin_CallsPreferencesBeanInit() {
        User user = new User();
        user.setName("testuser");

        loginObserver.observeLogin(user);

        verify(preferencesBean).init("testuser");
    }

    @Test
    public void testObserveLogin_NotNull() {
        assertNotNull(loginObserver);
    }
}
